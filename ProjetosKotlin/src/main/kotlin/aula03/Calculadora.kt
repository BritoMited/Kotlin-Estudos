package aula03

class Calculadora {
    companion object{
        fun somarDeDoisValoresInteirosNumero1eNumero2QueONumero2EAdicionadoAoNumero1(n1 : Int, n2 : Int): Int{
            return n1 + n2
        }

        fun subtrairDeDoisValoresInteirosNumero1eNumero2QueONumero2ESubtraidoDoNumero1(n1 : Int, n2 : Int): Int{
            return n1 - n2
        }
    }
}